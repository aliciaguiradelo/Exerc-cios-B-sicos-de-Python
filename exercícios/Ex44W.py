soma=0
while(i<=10):
    n=int(input("Digite um número:"))
    while(n<0):
        n=int(input("Digite outro numero:"))
    if(i==1):
        maior = n
    elif (n>maior):
        maior=n
    soma=soma+n
    i=i+1
